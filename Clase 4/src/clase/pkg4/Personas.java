package clase.pkg4;

/**
 *
 * @author s204e2
 */
public class Personas {
    private String nombre;
    private String ciudad;
    private int edad;
    private int cedula;

    public Personas(String nombre, String ciudad, int edad, int cedula) {
        this.nombre = nombre;
        this.ciudad = ciudad;
        this.edad = edad;
        this.cedula = cedula;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getCiudad() {
        return ciudad;
    }

    public void setCiudad(String ciudad) {
        this.ciudad = ciudad;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public int getCedula() {
        return cedula;
    }

    public void setCedula(int cedula) {
        this.cedula = cedula;
    }

    
    
}
